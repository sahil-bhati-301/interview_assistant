def get_questions(domain: str, difficulty: str, count: int = 5):
    """Get random questions from the question bank"""
    import random

    if domain not in QUESTION_BANK:
        raise ValueError(f"Domain '{domain}' not found in question bank")

    if difficulty not in QUESTION_BANK[domain]:
        raise ValueError(f"Difficulty '{difficulty}' not found for domain '{domain}'")

    questions = QUESTION_BANK[domain][difficulty]
    selected_questions = random.sample(questions, min(count, len(questions)))

    return selected_questions

def get_all_domains():
    """Get list of all available domains"""
    return list(QUESTION_BANK.keys())

def get_domain_difficulties(domain: str):
    """Get available difficulties for a domain"""
    if domain not in QUESTION_BANK:
        return []
    return list(QUESTION_BANK[domain].keys())